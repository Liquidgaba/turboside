export const garrettTurbos: GarrettTurbo[] = RAW.split("\n")
  .map((line) => line.trim())
  .filter(Boolean)
  .map((line) => {
    const [serie = "", delnummer = ""] = line.split(",").map((s) => s.trim());
    return { serie, delnummer };
  })
  .filter((t) => t.serie && t.delnummer);

export function turboToSlug(t: GarrettTurbo): string {
  return slugFromTurbo(t);
}

export function getGarrettTurboBySlug(slug: string): GarrettTurbo | undefined {
  return garrettTurbos.find((t) => slugFromTurbo(t) === slug);
}

export function getAllGarrettSlugs(): string[] {
  return garrettTurbos.map(slugFromTurbo);
}

/** Gruppert etter serie for listeside. */
export function getGarrettTurbosBySeries(): Record<string, GarrettTurbo[]> {
  const out: Record<string, GarrettTurbo[]> = {};
  for (const t of garrettTurbos) {
    if (!out[t.serie]) out[t.serie] = [];
    out[t.serie].push(t);
  }
  return out;
}

